namespace DM.Client.WPF.Controls.MessageBox
{
    /// <summary>
    /// 窗体位置
    /// </summary>
    public class WindowPosition
    {
        /// <summary>
        /// 屏幕左边位置
        /// </summary>
        public double Left { get; set; }


        /// <summary>
        /// 屏幕上边位置
        /// </summary>
        public double Top { get; set; }


        /// <summary>
        /// 当前窗体高度备份
        /// </summary>
        public int Height { get; set; }


        /// <summary>
        /// 当前窗体宽度备份
        /// </summary>
        public int Width { get; set; }
    }
}